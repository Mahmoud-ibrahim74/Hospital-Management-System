    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String getID = id.getText();
        String getDep = department.getSelectedItem().toString();
        String getFname = fname.getText();
        String getLname = lname.getText();
        String getAge = age.getText();
        String getBlood = blood.getSelectedItem().toString();
        String getGender = gender.getSelectedItem().toString();
        String getPhone = phone.getText();
        String getEmail = email.getText();
        String getAddress = address.getText();
        String getCity = city.getText();
        String getDays = days.getText();
        String getjoin = join.getText();
        String getLeave = leave.getText();
        String getUsername = username.getText();
        String getPassword = password.getText();

        if (getID.isEmpty() || getDep.isEmpty() || getFname.isEmpty() || getLname.isEmpty() || getAge.isEmpty() || getBlood.isEmpty() || getBlood.isEmpty() || getGender.isEmpty() || getPhone.isEmpty() || getEmail.isEmpty() || getAddress.isEmpty() || getCity.isEmpty() || getDays.isEmpty() || getjoin.isEmpty() || getLeave.isEmpty() || getUsername.isEmpty() || getPassword.isEmpty()) {
            Toolkit.getDefaultToolkit().beep();   // alert
            JOptionPane.showMessageDialog(this, "please Fill all Fileds   !!", "Failure", JOptionPane.ERROR_MESSAGE);

        }
        if (s == "null") {
            Toolkit.getDefaultToolkit().beep();   // alert
            JOptionPane.showMessageDialog(this, "Plaes insert New photo   !!", "Failure", JOptionPane.ERROR_MESSAGE);
        } else {
            try {

                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hms", "root", "");
                String sqlStmt = "INSERT INTO `doctor`(`id`, `department`, `fname`, `lname`, `age`, `blood`, `gender`, `phone`, `email`, `address`, `city`, `days`, `joinDate`, `leaveDate`, `username`, `password`,image) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                PreparedStatement stmt = con.prepareStatement(sqlStmt);
                InputStream Is = new FileInputStream(new File(s));
                stmt.setString(1, getID);
                stmt.setString(2, getDep);
                stmt.setString(3, getFname);
                stmt.setString(4, getLname);
                stmt.setString(5, getAge);
                stmt.setString(6, getBlood);
                stmt.setString(7, getGender);
                stmt.setString(8, getPhone);
                stmt.setString(9, getEmail);
                stmt.setString(10, getAddress);
                stmt.setString(11, getCity);
                stmt.setString(12, getDays);
                stmt.setString(13, getjoin);
                stmt.setString(14, getLeave);
                stmt.setString(15, getUsername);
                stmt.setString(16, getPassword);
                stmt.setBlob(17, Is);
                stmt.execute();
                JOptionPane.showMessageDialog(this, "Your Record is added sucessfully :)");
                resetData();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Failure", JOptionPane.ERROR_MESSAGE);

            } catch (FileNotFoundException ex) {

                JOptionPane.showMessageDialog(this, ex.getMessage(), "Failure", JOptionPane.ERROR_MESSAGE);

            }

        }
    }//GEN-LAST:event_addActionPerformed

